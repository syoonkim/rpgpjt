package Han;

public class Han {
	
	private boolean[] badges;
	private String[] bg;
	private int pkcnt;
	private int money;
	
	
	public boolean[] getBadges() {
		return badges;
	}
	public void setBadges(boolean[] badges) {
		this.badges = badges;
	}
	public int getPkcnt() {
		return pkcnt;
	}
	public void setPkcnt(int pkcnt) {
		this.pkcnt = pkcnt;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public String[] getBg() {
		return bg;
	}
	public void setBg(String[] bg) {
		this.bg = bg;
	}
	
	

}
